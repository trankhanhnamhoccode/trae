const sanitizeForJsString = (value) => {
  return String(value || '')
    .replace(/\\/g, '\\\\')
    .replace(/`/g, '\\`')
    .replace(/\$/g, '\\$');
};

const makePackageJson = (project) => {
  const safeName = String(project.title || 'generated-app')
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-|-$/g, '') || 'generated-app';

  return JSON.stringify(
    {
      scripts: {
        dev: 'vite --host 0.0.0.0',
        build: 'vite build',
        preview: 'vite preview'
      },
      dependencies: {
        '@vitejs/plugin-react': 'latest',
        vite: 'latest',
        react: 'latest',
        'react-dom': 'latest'
      },
      devDependencies: {},
      name: safeName,
      version: '0.1.0',
      private: true
    },
    null,
    2
  );
};

const makeAppJsx = (project) => {
  const title = sanitizeForJsString(project.title);
  const prompt = sanitizeForJsString(project.prompt);
  const appType = sanitizeForJsString(project.appType || 'web-app');
  const modelType = sanitizeForJsString(project.modelType || 'balanced');

  return `import './index.css';

const generatedSections = [
  'Clear hero section based on the original user prompt',
  'Feature cards for the main product value',
  'Call-to-action area for conversion',
  'Responsive layout for desktop and mobile'
];

function App() {
  return (
    <main className="app-shell">
      <section className="hero-card">
        <p className="eyebrow">AI Software Builder Preview</p>
        <h1>${title}</h1>
        <p className="prompt-summary">${prompt}</p>
        <div className="meta-row">
          <span>Type: ${appType}</span>
          <span>Mode: ${modelType}</span>
        </div>
        <button className="primary-button">Explore Generated App</button>
      </section>

      <section className="feature-grid">
        {generatedSections.map((section) => (
          <article className="feature-card" key={section}>
            <h2>{section}</h2>
            <p>This block is generated by the fake AI service and can be replaced by real AI output later.</p>
          </article>
        ))}
      </section>
    </main>
  );
}

export default App;
`;
};

