const generateProject = async (project) => {
  // This service is intentionally fake for the MVP.
  // Later, replace this function body with a real AI provider call while keeping the same return shape.
  const plan = [
    `Analyze the prompt and identify the target app type: ${project.appType || 'web-app'}.`,
    'Create a responsive React/Vite project structure.',
    'Generate a hero section, feature grid, and conversion call-to-action.',
    'Prepare source files that the frontend can display in a code viewer.',
    'Return a preview description and mark the project as ready.'
  ];

  const files = [
    {
      path: 'package.json',
      language: 'json',
      content: makePackageJson(project)
    },
    {
      path: 'src/App.jsx',
      language: 'jsx',
      content: makeAppJsx(project)
    },
    {
      path: 'src/main.jsx',
      language: 'jsx',
      content: makeMainJsx()
    },
    {
      path: 'src/index.css',
      language: 'css',
      content: makeIndexCss()
    }
  ];

  return {
    plan,
    files,
    previewDescription: `A polished ${project.appType || 'web-app'} preview generated from: "${project.prompt}"`
  };
};

